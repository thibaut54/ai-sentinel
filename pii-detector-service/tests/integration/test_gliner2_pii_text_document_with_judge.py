def test_Should_ParseDocumentWithTpAndFp_When_AnnotationsLoaded():
    """Sanity check that runs in any venv: the document + sidecar parse and
    carry both TP and FP annotated lines."""
    scan_document, annotations, positions = _parse_document(
        GLINER2_DOCUMENT_PATH, GLINER2_ANNOTATIONS_PATH
    )
    assert scan_document, "scan_document must not be empty"
    assert len(annotations) == len(positions)
    per_verdict: Dict[str, int] = defaultdict(int)
    for ann in annotations:
        per_verdict[ann.verdict] += 1
    assert per_verdict["TP"] > 0 and per_verdict["FP"] > 0, (
        f"document must contain both TP and FP lines (got {dict(per_verdict)})"
    )
    log.info(
        "[meta] %d annotated lines (%d TP / %d FP)",
        len(annotations), per_verdict["TP"], per_verdict["FP"],
    )
