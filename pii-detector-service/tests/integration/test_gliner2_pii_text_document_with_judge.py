"""Document-wide FP/FN evaluation of **GLiNER2** + the LLM judge on a single
plain-text document.

GLiNER2 counterpart of
:mod:`tests.integration.test_openmed_pii_text_document_with_judge`. It scans a
**dedicated** Confluence-like document
(``tests/resources/gliner2-pii-test-document.txt`` + its sidecar
``gliner2-pii-test-document.annotations.json``, generated by
``tests/resources/gliner2-fp-eval/_generate_text_document.py``) with the
GLiNER2 detector (the ``gliner2`` package), parametrized over the two
checkpoints under evaluation (``gliner2-large-v1`` and the PII fine-tune).

Scoping
-------

The document is assembled from the **24 evaluated GLiNER2 types** only (the
agreed privacy-filter subset), so every annotated line belongs to one of those
types — no out-of-list buckets. The document-wide scorer (reused from the
OpenMed sibling via :func:`_parse_document`) is **type-agnostic**: a detection
counts as a TP when it overlaps the annotated PII *value* on a TP line
(IoU >= 0.5) and as an FP elsewhere — the detector's own label is never
compared to the annotation's ``pii_type``.

Assertions (document-wide, per model)
-------------------------------------

Only ONE hard gate document-wide — recall-degradation is SOFT here (it is HARD
in the per-type harness):

* **HARD — judge must not worsen precision**: ``judged_fp_rate <=
  baseline_fp_rate``. The judge removes entities only, so a regression means it
  dropped TPs faster than FPs. Both checkpoints pass — the judge is a strong
  precision booster (it clears the large majority of GLiNER2's FPs).
* **SOFT (WARN) — recall degradation, absolute FP cap (0.10), recall floor
  (0.50)**. Document-wide recall/precision are distorted by the
  one-value-per-line annotation (GLiNER2 also surfaces real PII the sidecar
  doesn't list — e.g. extra expiry/CVV on a card line — which inflates both FP
  and apparent TP-loss) and by values the document frames as test/example data
  that the judge rightly discounts. So recall erosion here measures the
  fixture+detector interaction, not a judge safety defect, and is
  reported rather than gated.

Run (dedicated venv — see tests/resources/gliner2-fp-eval/README.md):

    .venv-gliner2/Scripts/python.exe -m pytest \\
        tests/integration/test_gliner2_pii_text_document_with_judge.py \\
        -v -s --log-cli-level=INFO --no-cov

Skipped automatically when the ``gliner2`` package is missing or LM Studio is
unreachable.
"""

