@pytest.fixture(scope="module")
def judge_validator():
    """Real ``LLMJudgeValidator`` auditing GLiNER-sourced entities.

    ``max_workers=1`` is mandatory here (not a tuning choice), exactly as in the
    OpenMed document-wide sibling: this test scans ONE big document, so a single
    ``filter()`` call submits all of the document's detections at once. LM
    Studio splits its context window across parallel slots; 3+ concurrent judge
    requests overflow a slot and return HTTP 400, which ``fail_open=True`` would
    silently swallow (keeping every entity and defeating the FP assertions).
    Serialising gives every call the full context window.
    """
    from pii_detector.infrastructure.validation.llm_validator import (
        LLMJudgeValidator,
    )

    log.info(
        "[setup] building LLMJudgeValidator(audit_sources={GLINER}, max_workers=1) on %s",
        BASE_URL,
    )
    validator = LLMJudgeValidator(
        base_url=BASE_URL,
        audit_sources={DetectorSource.GLINER},
        fail_open=True,
        max_workers=1,
    )
    try:
        yield validator
    finally:
        validator.shutdown()


