@_runtime
def test_Should_NotWorsenFpRate_When_JudgeIsAppliedOnGliner2PlainTextDocument(
    parsed_document, gliner2_detector, judge_validator
):
    """Scan the whole document with GLiNER2, run the judge, compare baseline vs
    judged metrics. Hard gates assert the judge's safety (no precision
    regression, bounded recall erosion); absolute FP/recall are SOFT signals.

    Every detection is logged with its line + KEPT/DROP + TP/FP; FP-survivors
    and TP-loss outcomes are promoted to WARNING.
    """
    scan_document, annotations, positions = parsed_document
    model_id = gliner2_detector.model_id
    annotation_by_line = {a.line_number: a for a in annotations}
    line_text_by_number = {a.line_number: a.text for a in annotations}

    # --- 1. GLiNER2 scan over the full document ------------------------
    log.info(
        "[scan|%s] running GLiNER2 on %d-char document (%d annotated lines) "
        "at threshold=%.2f",
        model_id, len(scan_document), len(annotations), GLINER2_DOC_THRESHOLD,
    )
    t0 = time.time()
    raw_entities = gliner2_detector.detect(scan_document, threshold=GLINER2_DOC_THRESHOLD)
    log.info(
        "[scan|%s] GLiNER2 produced %d entities in %.1fs",
        model_id, len(raw_entities), time.time() - t0,
    )

    # --- 2. Apply the LLM judge ----------------------------------------
    t0 = time.time()
    judged_entities = (
        judge_validator.filter(scan_document, raw_entities) if raw_entities else []
    )
    log.info(
        "[judge|%s] judged set: %d entities (out of %d baseline) in %.1fs",
        model_id, len(judged_entities), len(raw_entities), time.time() - t0,
    )
    judged_spans = {(e.start, e.end) for e in judged_entities}

    # --- 3. Per-entity verdict + structured logging --------------------
    metrics = DocumentMetrics()
    matched_tp_lines_baseline: set = set()
    matched_tp_lines_judged: set = set()

    for ent in raw_entities:
        line_pos = _resolve_line_for_offset(positions, ent.start, ent.end)
        if line_pos is None:
            continue  # detection outside any annotated line — not scored
        annotation = annotation_by_line[line_pos.line_number]
        line_text = line_text_by_number[line_pos.line_number]

        ground_truth = "FP"
        if annotation.verdict == "TP" and annotation.expected_value:
            for ex_start, ex_end in _value_offsets_on_line(
                line_text, line_pos, annotation.expected_value
            ):
                if _iou(ent.start, ent.end, ex_start, ex_end) >= IOU_MATCH_THRESHOLD:
                    ground_truth = "TP"
                    break

        action = "KEPT" if (ent.start, ent.end) in judged_spans else "DROP"
        pii_type = annotation.pii_type
        bucket = metrics.per_type[pii_type]

        if ground_truth == "TP":
            metrics.baseline_tp += 1
            bucket["b_tp"] += 1
            matched_tp_lines_baseline.add(line_pos.line_number)
            if action == "KEPT":
                metrics.judged_tp += 1
                bucket["j_tp"] += 1
                matched_tp_lines_judged.add(line_pos.line_number)
            else:
                metrics.judged_fn += 1
                bucket["j_fn"] += 1
                metrics.tp_loss.append(
                    (line_pos.line_number, pii_type,
                     scan_document[ent.start:ent.end], line_text[:200])
                )
        else:
            metrics.baseline_fp += 1
            bucket["b_fp"] += 1
            if action == "KEPT":
                metrics.judged_fp += 1
                bucket["j_fp"] += 1
                metrics.fp_survivors.append(
                    (line_pos.line_number, pii_type,
                     scan_document[ent.start:ent.end], line_text[:200])
                )

        is_bad = (action == "KEPT" and ground_truth == "FP") or (
            action == "DROP" and ground_truth == "TP"
        )
        flag = (
            "  <-- FP-survivor" if (action == "KEPT" and ground_truth == "FP")
            else ("  <-- TP-loss" if (action == "DROP" and ground_truth == "TP") else "")
        )
        (log.warning if is_bad else log.info)(
            "[%s line=%d %s/%s] %s | %s | %r @%d:%d score=%.2f%s",
            model_id, line_pos.line_number, pii_type, annotation.verdict,
            action, ground_truth, scan_document[ent.start:ent.end],
            ent.start, ent.end, float(ent.score), flag,
        )

    # --- 4. FN: TP lines GLiNER2 missed entirely -----------------------
    for ann in annotations:
        if ann.verdict != "TP":
            continue
        bucket = metrics.per_type[ann.pii_type]
        if ann.line_number not in matched_tp_lines_baseline:
            metrics.baseline_fn += 1
            bucket["b_fn"] += 1
            if ann.line_number not in matched_tp_lines_judged:
                metrics.judged_fn += 1
                bucket["j_fn"] += 1

    # --- 5. Document-wide + per-type summary ---------------------------
    log.info(
        "[document|%s] baseline tp=%d fp=%d fn=%d FP_rate=%.3f recall=%.3f "
        "| judged tp=%d fp=%d fn=%d FP_rate=%.3f recall=%.3f",
        model_id,
        metrics.baseline_tp, metrics.baseline_fp, metrics.baseline_fn,
        metrics.baseline_fp_rate, metrics.baseline_recall,
        metrics.judged_tp, metrics.judged_fp, metrics.judged_fn,
        metrics.judged_fp_rate, metrics.judged_recall,
    )
    for pii_type in sorted(metrics.per_type):
        b = metrics.per_type[pii_type]
        log.info(
            "[type=%s|%s] baseline tp=%d fp=%d fn=%d | judged tp=%d fp=%d fn=%d",
            pii_type, model_id,
            b["b_tp"], b["b_fp"], b["b_fn"], b["j_tp"], b["j_fp"], b["j_fn"],
        )
    if metrics.fp_survivors:
        log.warning(
            "[document|%s] FP-survivors (%d): %s", model_id, len(metrics.fp_survivors),
            "; ".join(
                f"line={ln} {tp}: {val!r}"
                for ln, tp, val, _ in metrics.fp_survivors[:10]
            ),
        )
    if metrics.tp_loss:
        log.warning(
            "[document|%s] TP-loss (%d): %s", model_id, len(metrics.tp_loss),
            "; ".join(
                f"line={ln} {tp}: {val!r}"
                for ln, tp, val, _ in metrics.tp_loss[:10]
            ),
        )

    # --- 6. Soft signals + hard gates ----------------------------------
    if metrics.judged_fp_rate > WARN_FP_RATE_WITH_JUDGE:
        log.warning(
            "[document|%s] judged FP_rate=%.3f above the soft cap %.2f "
            "(baseline=%.3f) — detector precision signal.",
            model_id, metrics.judged_fp_rate, WARN_FP_RATE_WITH_JUDGE,
            metrics.baseline_fp_rate,
        )
    if metrics.judged_recall < MIN_RECALL:
        log.warning(
            "[document|%s] judged recall=%.3f below soft floor %.2f "
            "(baseline=%.3f) — detector-level gap, not a judge defect.",
            model_id, metrics.judged_recall, MIN_RECALL, metrics.baseline_recall,
        )
    if metrics.recall_degradation > MAX_RECALL_DEGRADATION:
        # SOFT on the document (unlike the per-type harness): the document-wide
        # recall is distorted by the one-value-per-line annotation (GLiNER2 also
        # finds real PII the sidecar doesn't list, inflating FP and the apparent
        # TP-loss) and by values the document frames as test/example data that
        # the judge rightly discounts. So recall erosion here measures the
        # fixture+detector interaction, not a judge safety defect.
        log.warning(
            "[document|%s] judge eroded recall by %.3fpp (%.3f -> %.3f), above "
            "the %.2fpp budget — SOFT on the document (annotation granularity / "
            "test-framed values). First TP-loss: %s",
            model_id, metrics.recall_degradation, metrics.baseline_recall,
            metrics.judged_recall, MAX_RECALL_DEGRADATION,
            "; ".join(f"line={ln} {tp}: {val!r}" for ln, tp, val, _ in metrics.tp_loss[:5]),
        )

    # The only HARD gate document-wide: the judge must not WORSEN precision.
    # It removes entities only, so a regression means it dropped TPs faster than
    # FPs. Both checkpoints satisfy this (the judge is a strong precision
    # booster); absolute FP, recall floor and recall degradation are SOFT.
    assert metrics.judged_fp_rate <= metrics.baseline_fp_rate + 1e-9, (
        f"[{model_id}] judge WORSENED the document FP rate "
        f"({metrics.baseline_fp_rate:.3f} -> {metrics.judged_fp_rate:.3f}): it "
        f"removed true positives faster than false ones. First TP-loss lines: "
        + "; ".join(
            f"line={ln} {tp}: {val!r}" for ln, tp, val, _ in metrics.tp_loss[:3]
        )
    )


# ---------------------------------------------------------------------------
# Meta-test: document parses (independent of GLiNER2 and LM Studio)
